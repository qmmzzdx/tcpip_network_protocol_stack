#!/bin/bash

# 使用 exec 命令替换当前 shell 进程为 make 命令
# -j$(nproc) 选项告诉 make 使用与系统 CPU 核心数相同的并行任务数
# "$@" 将所有传递给脚本的参数传递给 make 命令
exec make -j$(nproc) "$@"

# 功能说明：
# 1. 并行构建：通过 -j$(nproc) 选项，make 将根据系统的 CPU 核心数并行执行构建任务，
#    这可以显著加快构建过程，特别是在大型项目中。
#
# 2. 参数传递：使用 "$@" 允许用户在调用脚本时传递额外的参数，这些参数会被直接传递给 make 命令。
#    例如，用户可以运行 ./make-parallel.sh clean 来执行 make clean。
#
# 3. 适用性：该脚本适用于需要使用 make 工具进行构建的项目，用户只需运行该脚本即可开始构建过程。
